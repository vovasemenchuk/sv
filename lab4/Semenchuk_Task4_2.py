print ('[1] Створення прикладних програм на мові Python, Лабораторна #4.1')
print ('[2] Семенчук Володимир, №Заліковки')
number = int(input ('[3]	Введіть число: '))
t = 1
k = 0
while  True:
	if number / t < 1:
		break
	else:
		t *= 10
		k += 1
print('	Кільскість цифр - ', k)
